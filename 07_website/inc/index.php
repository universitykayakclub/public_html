			<p>The University Kayak Club (UKC) is a sports club sponsored by the <a href="http://depts.washington.edu/ima/">University of Washington's Department of Recreational Sports Programs</a>. The Club promotes a wide variety of human-powered boating activities. Over the past several years, most club members have been involved in recreational whitewater and ocean kayaking, but some have participated in related sports such as canoeing and even dragon boat racing. Traditionally, members of the club have competed in whitewater slalom racing, kayak polo, and sea kayak races. Boaters of all skill levels are welcome, and those who are new to the sport are especially encouraged to join.</p>
			<p>The Pacific Northwest is one of the best areas in the country for canoeing and kayaking. We enjoy a year round season, with easy access to a multitude of paddling opportunities. For river kayakers, there are over 15 good whitewater runs within a 90 minute drive of Seattle. Local rainfall throughout the fall and winter and snowmelt through the spring and summer keeps nearby rivers flowing for most months of the year. There are also several whitewater slalom races within a couple hours drive of Seattle. Sea kayakers enjoy many nearby islands, especially the San Juans, that provide an abundance of prime destinations.</p>
			
