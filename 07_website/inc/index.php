<h2>ACA Intro to Whitewater Course</h2>
<p>
This April's intro to Whitewater Course is now open for registration.  Class is limited to the first 12 enrollees, we will add additional courses for the spring/summer as demand warrants. 
</p> 
<p>This course is designed for novice paddlers, but will be beneficial for any beginner or sea kayak crossover.  The course consists of one evening in the classroom, followed by two full days on the water.  On the water we will quickly progress from flat water to moving flat water to class I and II rapids.  Paddle strokes, maneuvering, river reading, and river running skills will be emphasized.  The course consists of three sessions on Friday April 10 (6pm-8pm), Saturday April 11 (all day), Saturday April 18 (all day).
</p> 
