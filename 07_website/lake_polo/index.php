			<h2>General Info</h2>
			<p>The UKC has informal kayak polo games all year round.  These games occur once a week on Tuesdays at 5:45 PM meeting at the Waterfront Activities Center (WAC) on Lake Washington.  It is a great opportunity to bring together all club kayakers.  Non-UKC members are also encouraged to come on and play, but you must provide all of your own kayaking gear.  Beginning paddlers are especially encouraged to try kayak polo because it is a great way to meet other club kayakers, make contact with trip coordinators, and work on paddling skills while having a lot of fun.  So, watch for kayak polo announcements and come on down to have a great time.</p>
			<h2>How to Play</h2>
			<p>The game involves lots of teamwork because you cannot paddle while holding onto the ball and you can only hold the ball for more than five seconds.  While one team is passing the ball and trying to score, the other team defends their goal and tries to gain possession of the ball.</p>
			<p>Kayak polo is similar to water polo. Polo players are divided into two teams. Up to five players from each team play at a time. They paddle around and try to put the ball through the goal of the opposing team.</p>
			<h2>Rules</h2>
			<p>The Official International Canoe Federation Rules are used by the club as a basic guideline. However, UKC polo is intended to be less competitive and more fun so the official rules are followed quite loosely.</p>
			<p>The club has a few of its <a href="lake_polo/polo_rules.php">own polo rules</a>. The ones relating to safety are followed rigorously.</p>
			<p>There is usually someone serving as referee during kayak polo games to help keep it a safe, fun, and fair game for all.</p>
