			<h1>Whitewater Checklist</h1>
			<h2>What you should bring on River Trips</h2>
			<ul class="checkboxes">
				<li>Wool, pyle or polypro sweater/shirt and pants to wear under a wet suit. <strong>Cotton will not be appropriate!</strong></li>
				<li>Old sneakers you don't mind getting wet (see neoprene booties remark below.) Wear wool socks inside these, not cotton!</li>
				<li>A large towel is a good idea, and can be used as a "Modesty Shroud" by the shy.</li>
				<li>A few dollars for gas. Exactly how much depends upon the distance of the drive, the number of riders contributing, etc. If you have doubts, ask the driver what seems fair. (Remember, dragging boats around also makes for a significant amount of wear and tear on a vehicle, which can be more costly than petrol.) Drivers of big pickup trucks that carry lots of gear but also suck gas should get a buck from non-passengers as well.</li>
				<li>Often we stop for food on the way to or from the river. A meal, or money for a meal, is a good idea.</li>
				<li>Food and water for the river. (Bad things happen to dehydrated, hypothermic boaters.)</li>
				<li>A raincoat. (The river gods always rain on beginner trips)</li>
				<li>A full set of warm dry clothes, separate and independent of what you are wearing on the river. The clothes that you wear in the car on the way to the river usually count for this.</li>
				<li>For the nasally challanged, nose plugs.</li>
				<li>For the ear canal challanged, ear plugs.</li>
				<li>Flashlight. (Useful if dark at the take-out.)</li>
				<li>Chocolate for the trip coordinator(s). (This is optional, of course, but it is in your interests to make sure that your trip coordinators like you, you get rescued quicker and you get invited on more trips that way, just ask Tiana.)</li>
				<li>A vehicle with racks suitable for carrying boats is often very helpful. Sometimes, a car without racks, is useful too.</li>
			</ul>
